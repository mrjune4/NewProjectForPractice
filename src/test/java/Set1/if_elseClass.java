package Set1;

public class if_elseClass {
    public static void main(String[] args) {
        int x = 10;
        double y = 10.1;
        if (x>y){
            System.out.println("X is greater");
        } else if (y>x) {
            System.out.println("Y is greater");
        } else {
            System.out.println("Both Numbers are equal");
        }
    }
}
